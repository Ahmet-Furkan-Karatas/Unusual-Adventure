def karakter_hareketi():
    global karakter_x_hiz, karakter_y_hiz, yer_cekimi, ziplama, carpisma, ziplandi, eski_x, eski_y, merdivene_degiliyor, odul_hizlanma

    if carpisma_kontrolu():
        yer_cekimi = 2
        # karakter.y-=1

    if not carpisma_kontrolu():
        karakter.y += yer_cekimi
        if yer_cekimi <= 10:
            yer_cekimi += 0.4

    if merdivene_degiliyor:
        yer_cekimi = 0

    if karakter.collidelist(sol_yan_zeminler)!=-1 or karakter.colliderect(zemin3_1):
        karakter.x = eski_x

    if karakter.collidelist(sag_yan_zeminler)!=-1:
        karakter.x = eski_x

    if karakter.colliderect(hucre157):
        karakter.x -= 1

    if keyboard.left:
        if karakter.x > 1 and karakter_x_hiz > -5.9 and not karakter.colliderect(dusman):
            eski_x = karakter.x
            eski_y = karakter.y
            karakter_x_hiz -= odul_hizlanma
            karakter.image = "karakter_sol_1"

    elif keyboard.right:
        if karakter.x < 800 and karakter_x_hiz < 5.9 and not karakter.colliderect(hucre157) and not karakter.colliderect(dusman):
            eski_x = karakter.x
            eski_y = karakter.y
            karakter_x_hiz += odul_hizlanma
            karakter.image = "karakter_sag_1"

    karakter.x += karakter_x_hiz

    if keyboard.up and carpisma_kontrolu() and not ziplandi:
        ziplama = True
        ziplandi = True
        clock.schedule_unique(ziplama_yapildi, 0.4)
        karakter_y_hiz = ziplama_artis

    if ziplama and karakter_y_hiz > 25:
        karakter_y_hiz = karakter_y_hiz - ((100 - karakter_y_hiz)/2)
        karakter.y -= karakter_y_hiz / 3

    else:
        karakter_y_hiz = 0
        ziplama = False

    if karakter_x_hiz > 0:
        karakter_x_hiz -=1
    if karakter_x_hiz < 0:
        karakter_x_hiz +=1

    if karakter.x <1 or karakter.x > 800:
        karakter_x_hiz = 0

